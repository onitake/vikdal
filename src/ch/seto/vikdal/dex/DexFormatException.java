package ch.seto.vikdal.dex;

public class DexFormatException extends Exception {

	private static final long serialVersionUID = 8016701505986815415L;

	public DexFormatException() {
		super();
	}

	public DexFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public DexFormatException(String message) {
		super(message);
	}

	public DexFormatException(Throwable cause) {
		super(cause);
	}

}
