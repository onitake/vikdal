package ch.seto.vikdal.dex;

public class DexEncodingException extends RuntimeException {

	private static final long serialVersionUID = -8935994629826635774L;

	public DexEncodingException() {
		super();
	}

	public DexEncodingException(String message, Throwable cause) {
		super(message, cause);
	}

	public DexEncodingException(String message) {
		super(message);
	}

	public DexEncodingException(Throwable cause) {
		super(cause);
	}

}
